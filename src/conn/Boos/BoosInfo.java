package conn.Boos;

public class BoosInfo {
	private String BOOS_id; //boos登陆id
	private String BOOS_name;  //boos名字
	private String BOOS_password;   //登陆密码
	public String getBOOS_id() {
		return BOOS_id;
	}
	public void setBOOS_id(String bOOS_id) {
		BOOS_id = bOOS_id;
	}
	public String getBOOS_name() {
		return BOOS_name;
	}
	public void setBOOS_name(String bOOS_name) {
		BOOS_name = bOOS_name;
	}
	public String getBOOS_password() {
		return BOOS_password;
	}
	public void setBOOS_password(String bOOS_password) {
		BOOS_password = bOOS_password;
	}
}
