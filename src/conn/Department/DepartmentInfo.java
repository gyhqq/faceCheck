package conn.Department;

public class DepartmentInfo {
	private String DE_id;  //部门id
	private String DE_name;  //部门名称
	private String DE_income;  //部门营收
	private String DE_expenditure;  //部门支出
	private String DE_remark;  //备注
	public String getDE_id() {
		return DE_id;
	}
	public void setDE_id(String dE_id) {
		DE_id = dE_id;
	}
	public String getDE_name() {
		return DE_name;
	}
	public void setDE_name(String dE_name) {
		DE_name = dE_name;
	}
	public String getDE_income() {
		return DE_income;
	}
	public void setDE_income(String dE_income) {
		DE_income = dE_income;
	}
	public String getDE_expenditure() {
		return DE_expenditure;
	}
	public void setDE_expenditure(String dE_expenditure) {
		DE_expenditure = dE_expenditure;
	}
	public String getDE_remark() {
		return DE_remark;
	}
	public void setDE_remark(String dE_remark) {
		DE_remark = dE_remark;
	}
}
