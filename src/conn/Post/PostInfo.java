package conn.Post;

public class PostInfo {
	private String PO_id;  //岗位id
	private String PO_name;  //岗位名称
	private String PO_department;  //岗位所属部门
	private String PO_wages;  //岗位基本工资
	private String PO_remark;  //岗位备注
	public String getPO_id() {
		return PO_id;
	}
	public void setPO_id(String pO_id) {
		PO_id = pO_id;
	}
	public String getPO_name() {
		return PO_name;
	}
	public void setPO_name(String pO_name) {
		PO_name = pO_name;
	}
	public String getPO_department() {
		return PO_department;
	}
	public void setPO_department(String pO_department) {
		PO_department = pO_department;
	}
	public String getPO_wages() {
		return PO_wages;
	}
	public void setPO_wages(String pO_wages) {
		PO_wages = pO_wages;
	}
	public String getPO_remark() {
		return PO_remark;
	}
	public void setPO_remark(String pO_remark) {
		PO_remark = pO_remark;
	}
}
